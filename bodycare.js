function displayData(data) {
  document.querySelector("section").innerHTML = ""
  data.forEach(function (elem, index) {
    var main_box = document.createElement("div");
    main_box.setAttribute("id", "head")


    var img_div = document.createElement("div");
    img_div.setAttribute("id", "main-box")
    var img = document.createElement("img");
    



    var div1 = document.createElement("div");
    div1.setAttribute("id", "div1");

    var brd = document.createElement("p");
    brd.setAttribute("class", "start")
    var name = document.createElement("p");
    name.setAttribute("class", "name")
    var company = document.createElement("p");
    company.setAttribute("class", "company")


    var div2 = document.createElement("div");
    div2.setAttribute("id", "div2");
    var price = document.createElement("p");
    price.setAttribute("class", "price");


    var view = document.createElement("p");
    view.setAttribute("class", "view");

    var btn = document.createElement("button");
    btn.setAttribute("id", "button")
    btn.innerText = "ADD TO BAG";
    btn.addEventListener("click", function () {
      addToCart(ele)
    })




    img.setAttribute("src", elem.image_url);
    img_div.append(img);

    brd.innerText = elem.brand;
    name.innerText = elem.name;
    company.innerText = elem.com;
    div1.append(brd, name, company);

    price.innerText = "$" + elem.price;
    view.innerText = elem.view;
    div2.append(price, view);

    main_box.append(img_div, div1, div2, btn);

    document.querySelector("section").append(main_box);


  })



  function addToCart(ele) {
    container.push(ele);
    localStorage.setItem("container", JSON.stringify(container))

  }
}


